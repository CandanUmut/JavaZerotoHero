package unit06.array.day25;

public class ForEachExample {
    public static void main(String[] args) {
        int[]arr={1,2,3,4,5,6};
        for (int num :arr){
            System.out.println(num + " ");
        }

        String[]arr1={"aaa","bbb","ccc","umut"};
        for (String am:arr1){
            System.out.println(am);
        }
    }
}

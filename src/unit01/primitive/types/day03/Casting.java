package unit01.primitive.types.day03;

public class Casting {
    public static void main(String[] args) {
        //Casting is turning one type to another
        int a =(int)5.5;

        System.out.println(a);
        System.out.println(5.0/2);

        double x = 3.4;
        int y = (int)x;
        System.out.println(y);

    }
}

package unit01.primitive.types.day02;

public class SwapValues {
    public static void main(String[] args) {

        int num1 = 7;
        int num2 = 9;
        int num3 = num1;
        /*
        * swap the values
        * num 1 value to num2
        *
         */
        num1=num2;
        num2 = num3;

        System.out.println(num1);
        System.out.println(num2);




    }
}

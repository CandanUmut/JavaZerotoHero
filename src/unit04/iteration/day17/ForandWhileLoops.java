package unit04.iteration.day17;

public class ForandWhileLoops {
    public static void main(String[] args) {
        for (int i = 10; i >=0 ; i--) {
            System.out.println("Countdown" + i);

        }
        int i =10;
        while(i>=0){
            System.out.println("Countdown" + i);
            i--;
        }
    }
}
